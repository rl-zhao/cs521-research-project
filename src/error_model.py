"""
Error mapping models (Section 3.5 of the VideoChef paper).

VideoChef observes that the quality of a canary output (C) is systematically
lower than the quality of the corresponding full-frame output (F) for the same
approximation level.  Three increasingly expressive regression models map C???F:

  Model-C   ??? quadratic in canary quality alone
  Model-CA  ??? linear in canary quality + approximation levels
  Model-CAD ??? linear in canary quality + ALs + row-difference features

IRABaseline ??? pass-through (F = C); equivalent to the IRA approach that
              assumes canary and full-frame quality are identical.

All models follow a common predict(C, al_vector, D) interface so they can be
swapped without changing the search code.
"""

import pickle
from pathlib import Path
from typing import List, Optional

import numpy as np
from sklearn.linear_model import LinearRegression
from sklearn.preprocessing import PolynomialFeatures


# ?????? Model-C ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

class ModelC:
    """
    Quadratic regression: F = w0 + w1??C + w2??C??

    Uses only the canary quality score; does not require knowledge of the
    approximation levels applied.
    """

    def __init__(self):
        self._poly = PolynomialFeatures(degree=2, include_bias=True)
        self._reg  = LinearRegression(fit_intercept=False)
        self.fitted = False

    def fit(self, C: np.ndarray, F: np.ndarray) -> "ModelC":
        X = self._poly.fit_transform(C.reshape(-1, 1))
        self._reg.fit(X, F)
        self.fitted = True
        return self

    def predict(
        self,
        C: float,
        al_vector: Optional[List[int]] = None,
        D: Optional[np.ndarray] = None,
    ) -> float:
        if not self.fitted:
            return float(C)
        X = self._poly.transform([[C]])
        return float(self._reg.predict(X)[0])

    def predict_batch(
        self,
        C: np.ndarray,
        al_matrix: Optional[np.ndarray] = None,
        D_matrix: Optional[np.ndarray] = None,
    ) -> np.ndarray:
        if not self.fitted:
            return C.copy()
        X = self._poly.transform(C.reshape(-1, 1))
        return self._reg.predict(X)


# ?????? Model-CA ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

class ModelCA:
    """
    Linear regression: F = w ?? [C, a1, a2, ???, an]

    Incorporates the approximation levels so the model can account for the
    fact that different AL combinations produce different error relationships.
    """

    def __init__(self, n_filters: int = 3):
        self.n_filters = n_filters
        self._reg      = LinearRegression()
        self.fitted    = False

    def fit(
        self,
        C: np.ndarray,
        AL: np.ndarray,   # shape (N, n_filters)
        F: np.ndarray,
    ) -> "ModelCA":
        X = np.column_stack([C.reshape(-1, 1), AL])
        self._reg.fit(X, F)
        self.fitted = True
        return self

    def predict(
        self,
        C: float,
        al_vector: List[int],
        D: Optional[np.ndarray] = None,
    ) -> float:
        if not self.fitted:
            return float(C)
        x = np.array([[C] + list(al_vector)])
        return float(self._reg.predict(x)[0])

    def predict_batch(
        self,
        C: np.ndarray,
        al_matrix: np.ndarray,
        D_matrix: Optional[np.ndarray] = None,
    ) -> np.ndarray:
        if not self.fitted:
            return C.copy()
        X = np.column_stack([C.reshape(-1, 1), al_matrix])
        return self._reg.predict(X)


# ?????? Model-CAD ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

class ModelCAD:
    """
    Linear regression: F = w ?? [C, a1, ???, an, d_Y, d_U, d_V]

    Row-difference features D capture per-channel variation between adjacent
    image rows, which is correlated with how much quality loop-perforation
    will lose.  This is the best-performing model in the paper.
    """

    def __init__(self, n_filters: int = 3):
        self.n_filters = n_filters
        self._reg      = LinearRegression()
        self.fitted    = False

    def fit(
        self,
        C: np.ndarray,
        AL: np.ndarray,   # shape (N, n_filters)
        D: np.ndarray,    # shape (N, 3)
        F: np.ndarray,
    ) -> "ModelCAD":
        X = np.column_stack([C.reshape(-1, 1), AL, D])
        self._reg.fit(X, F)
        self.fitted = True
        return self

    def predict(
        self,
        C: float,
        al_vector: List[int],
        D: np.ndarray,
    ) -> float:
        if not self.fitted:
            return float(C)
        x = np.array([[C] + list(al_vector) + list(D)])
        return float(self._reg.predict(x)[0])

    def predict_batch(
        self,
        C: np.ndarray,
        al_matrix: np.ndarray,
        D_matrix: np.ndarray,
    ) -> np.ndarray:
        if not self.fitted:
            return C.copy()
        X = np.column_stack([C.reshape(-1, 1), al_matrix, D_matrix])
        return self._reg.predict(X)


# ?????? IRA baseline ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

class IRABaseline:
    """
    IRA pass-through: assumes F ??? C.

    This is the naive approach from the IRA paper [PLDI'16] that VIDEOCHEF
    improves upon.
    """

    fitted = True  # Always 'ready'

    def predict(
        self,
        C: float,
        al_vector: Optional[List[int]] = None,
        D: Optional[np.ndarray] = None,
    ) -> float:
        return float(C)

    def predict_batch(self, C: np.ndarray, **_) -> np.ndarray:
        return C.copy()


# ?????? Factory & persistence ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

_MODEL_CLASSES = {
    "C":   ModelC,
    "CA":  ModelCA,
    "CAD": ModelCAD,
    "IRA": IRABaseline,
}


def create_model(model_type: str, n_filters: int = 3):
    """Instantiate an error model by name."""
    if model_type not in _MODEL_CLASSES:
        raise ValueError(f"Unknown model '{model_type}'. "
                         f"Available: {sorted(_MODEL_CLASSES)}")
    cls = _MODEL_CLASSES[model_type]
    if model_type in ("CA", "CAD"):
        return cls(n_filters)
    return cls()


def save_model(model, path: str) -> None:
    with open(path, "wb") as fh:
        pickle.dump(model, fh)


def load_model(path: str):
    with open(path, "rb") as fh:
        return pickle.load(fh)


def evaluate_model_f1(
    model,
    C_test: np.ndarray,
    al_test: np.ndarray,
    D_test: np.ndarray,
    F_test: np.ndarray,
    threshold: float,
) -> float:
    """
    Compute the F-1 score as used in Table 2 of the VideoChef paper.

    A prediction is 'positive' when the predicted full quality F?? ??? threshold
    (i.e. the model says the approximation is acceptable).  We compare this
    against whether the actual quality F ??? threshold.
    """
    model_type = type(model).__name__
    if model_type == "ModelC":
        F_pred = model.predict_batch(C_test)
    elif model_type == "ModelCA":
        F_pred = model.predict_batch(C_test, al_test)
    elif model_type == "ModelCAD":
        F_pred = model.predict_batch(C_test, al_test, D_test)
    else:
        F_pred = model.predict_batch(C_test)

    actual_pos    = F_test  >= threshold
    predicted_pos = F_pred >= threshold

    tp = np.sum(actual_pos & predicted_pos)
    fp = np.sum(~actual_pos & predicted_pos)
    fn = np.sum(actual_pos & ~predicted_pos)

    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall    = tp / (tp + fn) if (tp + fn) > 0 else 0.0

    if precision + recall == 0:
        return 0.0
    return 2 * precision * recall / (precision + recall)
