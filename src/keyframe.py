"""
Key-frame detection strategies (Section 3.3 of the VideoChef paper).

VideoChef re-runs its approximation search only at key frames, amortising
the search cost over many non-key frames.  Three strategies are provided:

  'fixed'        ??? recalibrate every FIXED_KEYFRAME_INTERVAL frames
  'scene_change' ??? histogram-based abrupt scene-change detector on canary frames
  'iframe'       ??? use I-frame positions supplied by the video decoder
"""

from typing import List, Optional

import cv2
import numpy as np

import config
from src.canary import generate_canary


# ?????? Fixed interval ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

def detect_keyframes_fixed(n_frames: int, interval: int = None) -> List[int]:
    """Return frame indices 0, interval, 2*interval, ???"""
    if interval is None:
        interval = config.FIXED_KEYFRAME_INTERVAL
    return list(range(0, n_frames, interval))


# ?????? Scene-change detection ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

def detect_keyframes_scene_change(
    frames: List,
    threshold: float = None,
    canary_scale: int = None,
) -> List[int]:
    """
    Histogram-based scene-change detector using Y-channel canary frames.

    A scene change is detected when the sum of absolute histogram-bin
    differences between consecutive canary frames exceeds
    *threshold* ?? total_canary_pixels.  Only the first frame of each new
    scene is returned as a key frame.
    """
    if threshold is None:
        threshold = config.SCENE_CHANGE_THRESHOLD
    if canary_scale is None:
        canary_scale = config.CANARY_SCALE

    keyframes = [0]
    if len(frames) < 2:
        return keyframes

    def _y_canary(f):
        c = generate_canary(f, canary_scale)
        return cv2.cvtColor(c, cv2.COLOR_BGR2GRAY) if c.ndim == 3 else c

    prev_y    = _y_canary(frames[0])
    prev_hist = cv2.calcHist([prev_y], [0], None, [256], [0, 256]).flatten()
    n_pixels  = prev_y.size

    for i in range(1, len(frames)):
        curr_y    = _y_canary(frames[i])
        curr_hist = cv2.calcHist([curr_y], [0], None, [256], [0, 256]).flatten()

        hist_diff = np.abs(curr_hist - prev_hist).sum()
        if hist_diff > threshold * n_pixels:
            keyframes.append(i)

        prev_hist = curr_hist

    return keyframes


# ?????? I-frame selection ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

def detect_keyframes_iframe(
    iframe_positions: List[int],
    n_frames: int,
    fallback_interval: int = None,
) -> List[int]:
    """
    Use I-frame indices (from the video decoder) as key frames.
    Falls back to fixed-interval detection if no positions are available.
    """
    valid = [p for p in iframe_positions if 0 <= p < n_frames]
    if not valid:
        return detect_keyframes_fixed(n_frames, fallback_interval)
    return sorted(valid)


# ?????? Factory ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

_STRATEGIES = {
    "fixed":        detect_keyframes_fixed,
    "scene_change": detect_keyframes_scene_change,
    "iframe":       detect_keyframes_iframe,
}


def get_keyframe_detector(strategy: str):
    """Return the callable for a named key-frame detection strategy."""
    if strategy not in _STRATEGIES:
        raise ValueError(f"Unknown keyframe strategy '{strategy}'. "
                         f"Available: {sorted(_STRATEGIES)}")
    return _STRATEGIES[strategy]
