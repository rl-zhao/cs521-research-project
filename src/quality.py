def compute_quality(
    original: np.ndarray,
    processed: np.ndarray,
    metric: str = "psnr",
) -> float:
    """
    Compute a quality score between *original* and *processed*.

    Parameters
    ----------
    metric : 'psnr'  ???  Peak Signal-to-Noise Ratio (dB)
             'ssim'  ???  Structural Similarity Index
    """
    if metric == "psnr":
        return compute_psnr(original, processed)
    if metric == "ssim":
        return compute_ssim(original, processed)
    raise ValueError(f"Unknown metric '{metric}'. Use 'psnr' or 'ssim'.")


def get_quality_threshold(metric: str, psnr_threshold: float = None) -> float:
    """Return the default acceptable quality threshold for a metric."""
    if psnr_threshold is None:
        psnr_threshold = config.DEFAULT_PSNR_THRESHOLD
    return {
        "psnr": psnr_threshold,
        "ssim": config.SSIM_THRESHOLD,
        "vmaf": 70.0,
    }.get(metric, psnr_threshold)


def get_error_margin(metric: str) -> float:
    """Return the safety margin used during approximation search."""
    return {
        "psnr": config.ERROR_MARGIN_PSNR,
        "ssim": config.ERROR_MARGIN_SSIM,
    }.get(metric, config.ERROR_MARGIN_PSNR)


def quality_acceptable(value: float, threshold: float) -> bool:
    """True when *value* meets *threshold* (higher-is-better for all metrics)."""
    return value >= threshold
