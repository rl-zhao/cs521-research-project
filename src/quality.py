def compute_psnr(
    original: np.ndarray,
    processed: np.ndarray,
    max_val: float = 255.0,
) -> float:
    """
    Peak Signal-to-Noise Ratio in dB.

    Returns +??? for identical frames.  Higher is better.
    Formula: 20??log10(MAX) ??? 10??log10(MSE)
    """
    if original.shape != processed.shape:
        processed = cv2.resize(processed,
                               (original.shape[1], original.shape[0]),
                               interpolation=cv2.INTER_LINEAR)
    mse = np.mean(
        (original.astype(np.float64) - processed.astype(np.float64)) ** 2
    )
    if mse < 1e-10:
        return float("inf")
    return 20.0 * np.log10(max_val) - 10.0 * np.log10(mse)


# ?????? SSIM ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

def compute_ssim(original: np.ndarray, processed: np.ndarray) -> float:
    """
    Structural Similarity Index (SSIM).

    Returns a value in [-1, 1]; higher is better.
    Uses scikit-image if available, otherwise falls back to a manual
    Gaussian-weighted implementation.
    """
    if original.shape != processed.shape:
        processed = cv2.resize(processed,
                               (original.shape[1], original.shape[0]),
                               interpolation=cv2.INTER_LINEAR)
    try:
        from skimage.metrics import structural_similarity as _ssim
        if original.ndim == 3:
            return float(_ssim(original, processed,
                               channel_axis=2, data_range=255))
        return float(_ssim(original, processed, data_range=255))
    except ImportError:
        return _manual_ssim(original, processed)


def _manual_ssim(img1: np.ndarray, img2: np.ndarray) -> float:
    """Luminance-based SSIM fallback."""
    if img1.ndim == 3:
        img1 = cv2.cvtColor(img1, cv2.COLOR_BGR2GRAY)
        img2 = cv2.cvtColor(img2, cv2.COLOR_BGR2GRAY)

    img1 = img1.astype(np.float64)
    img2 = img2.astype(np.float64)
    C1 = (0.01 * 255) ** 2
    C2 = (0.03 * 255) ** 2

    ksize = (11, 11)
    sigma = 1.5
    mu1   = cv2.GaussianBlur(img1, ksize, sigma)
    mu2   = cv2.GaussianBlur(img2, ksize, sigma)

    mu1_sq  = mu1 ** 2
    mu2_sq  = mu2 ** 2
    mu1_mu2 = mu1 * mu2

    sig1_sq = cv2.GaussianBlur(img1 ** 2, ksize, sigma) - mu1_sq
    sig2_sq = cv2.GaussianBlur(img2 ** 2, ksize, sigma) - mu2_sq
    sig12   = cv2.GaussianBlur(img1 * img2, ksize, sigma) - mu1_mu2

    num = (2 * mu1_mu2 + C1) * (2 * sig12 + C2)
    den = (mu1_sq + mu2_sq + C1) * (sig1_sq + sig2_sq + C2)
    return float((num / den).mean())


# ?????? VMAF (optional, requires ffmpeg with libvmaf) ???????????????????????????????????????????????????????????????????????????????????????

def compute_vmaf(
    reference_frames: list,
    distorted_frames: list,
    fps: float = 30.0,
) -> Optional[float]:
    """
    Compute VMAF score by writing both frame sets to temp files and invoking
    ffmpeg with libvmaf.  Returns None if ffmpeg / libvmaf is unavailable.
    """
    from src.video_io import write_video

    with tempfile.TemporaryDirectory() as tmpdir:
        ref_path = str(Path(tmpdir) / "ref.mp4")
        dis_path = str(Path(tmpdir) / "dis.mp4")
        log_path = str(Path(tmpdir) / "vmaf.json")

        write_video(reference_frames, ref_path, fps)
        write_video(distorted_frames, dis_path, fps)

        cmd = [
            "ffmpeg", "-i", dis_path, "-i", ref_path,
            "-lavfi",
            f"libvmaf=log_fmt=json:log_path={log_path}",
            "-f", "null", "-", "-y",
        ]
        try:
            subprocess.run(cmd, capture_output=True, text=True, timeout=300,
                           check=True)
            with open(log_path) as fh:
                data = json.load(fh)
            return float(data["pooled_metrics"]["vmaf"]["mean"])
        except Exception:
            return None


